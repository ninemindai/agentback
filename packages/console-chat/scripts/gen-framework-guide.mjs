// Embed the AgentBack framework skill into the package so the chat dock can
// ground its agent regardless of where it runs (the monorepo, or a published
// consumer app where skills/agentback is NOT on disk). Reads the canonical
// skills/agentback/SKILL.md, strips its YAML frontmatter (skill-discovery
// metadata, not needed in-context), and writes src/framework-guide.ts.
//
// The output is committed so the root `tsc -b` compiles it; this script reruns
// on `pnpm -F @agentback/console-chat build` to keep it in sync. After editing
// skills/agentback/SKILL.md, rebuild console-chat and commit the regenerated
// constant.

import {readFileSync, writeFileSync} from 'node:fs';
import {fileURLToPath} from 'node:url';
import {dirname, join} from 'node:path';

const here = dirname(fileURLToPath(import.meta.url)); // packages/console-chat/scripts
const repoRoot = join(here, '..', '..', '..');
const skillPath = join(repoRoot, 'skills', 'agentback', 'SKILL.md');
const outPath = join(here, '..', 'src', 'framework-guide.ts');

let body = '';
try {
  const raw = readFileSync(skillPath, 'utf8');
  // Strip a leading `---\n…\n---\n` YAML frontmatter block, if present.
  body = raw.replace(/^---\n[\s\S]*?\n---\n/, '').trim();
} catch (err) {
  console.warn(
    `gen-framework-guide: ${skillPath} not readable (${err.code ?? err.message}); emitting empty guide.`,
  );
}

const file = `// Copyright ninemind.ai 2026. All Rights Reserved.
// This file is licensed under the MIT License.
// License text available at https://opensource.org/license/mit/

// GENERATED by scripts/gen-framework-guide.mjs from skills/agentback/SKILL.md.
// Do not edit by hand — run \`pnpm -F @agentback/console-chat build\` to regenerate.

/**
 * The AgentBack framework skill (SKILL.md body, YAML frontmatter stripped),
 * embedded so the chat dock grounds its agent in any environment. Empty string
 * when the skill source was unavailable at build time.
 */
export const FRAMEWORK_GUIDE = ${JSON.stringify(body)};
`;

writeFileSync(outPath, file);
console.log(`gen-framework-guide: wrote ${outPath} (${body.length} chars)`);
